function test_1(testCase)
% Test function on 2x2x2 grid expanded by factor of 2.

inc = 2;
f_orig(:, :, 1) = [ ...
    [1, 2]; ...
    [3, 4] ...
];
f_orig(:, :, 2) = [ ...
    [5, 6]; ...
    [7, 8] ...
];

f_exp(:, :, 1) = [ ...
    [1, 1, 2, 2]; ...
    [1, 1, 2, 2]; ...
    [3, 3, 4, 4]; ...
    [3, 3, 4, 4]; ...
];
f_exp(:, :, 2) = [ ...
    [1, 1, 2, 2]; ...
    [1, 1, 2, 2]; ...
    [3, 3, 4, 4]; ...
    [3, 3, 4, 4]; ...
];
f_exp(:, :, 3) = [ ...
    [5, 5, 6, 6]; ...
    [5, 5, 6, 6]; ...
    [7, 7, 8, 8]; ...
    [7, 7, 8, 8]; ...
];
f_exp(:, :, 4) = [ ...
    [5, 5, 6, 6]; ...
    [5, 5, 6, 6]; ...
    [7, 7, 8, 8]; ...
    [7, 7, 8, 8]; ...
];

result = expand_grid_3d(f_orig, inc);
verifyEqual(testCase, result, f_exp);

end
